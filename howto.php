			<h1>Instructions:</h1>
			The game is open to all and free to play. Register or Login to play by submitting your predictions for the result, the scoreline and scorer(s) for all Chelsea matches.<br />
			You score points for every correct result, scoreline, and scorer you predict.<br />
			You will be entered into a monthly and seasonal league of players and ranked on your score points.<br />
			The player with the most points over the entire month or season and sits at the top of the league table will win the grand prize<br />
			
			<h1>Matches</h1>
			Predictions can be made for all Chelsea Barclays Premier League, UEFA Champions League, FA Cup and Carling Cup matches in the 2011-12 season.<br />
			
			<h1>Deadlines</h1>
			You can submit predictions at any time until 5 minutes before kick-off for the upcoming match.<br />
			Once submitted predictions can be updated until 5 minutes before kick-off.<br />
			Cup matches will be added within 24 hours of the draw being made, so make sure you log in regularly.<br />
			Points will be updated ASAP by the manager/admin, as soon as the official game results are released.<br />
			
			<h1>Points</h1>
			Points are awarded for each score prediction you submitted:<br />
			Correct result (win, lose or draw): 1 point<br />
			Exact score (eg. 2-0): 5 points<br />
			Each correct goal scorer: 1 point<br />
			
			<h1>Extra time</h1>
			Points are awarded for each score predictions based on the result at the end of a scheduled 90 minutes of play. <br />
			This includes any added injury or stoppage time but does not include extra-time, penalty shootouts or golden goals. <br />
			
			
